# Variables in Ruby
    # In order to refer to objects we want to assign them a name (variable)
    # You can give an object a name by using the "=" assignment operator
    # A name on the left side of the operator is assigned to the object on the right side of the assignment operator
    # The name is not the object itself, just a way that is used to reference the actual object
    number = 1

# Reusing variables
    # names are unique in ruby so names can only be used for one object
    # if you assign different values to a variable then the latest assignment will overwrite everything
    number = 2
        # the number name is now re-assigned to the value of 2 and is no longer 1
    
